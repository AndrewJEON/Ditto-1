        <b><font size="6">
        <a name="Features"></a></font><font size="5">
        Features</font></b>
    <ul>
      <li>
      <font size="3">
      Easy to use interface</font></li>
      <li>
      <font size="3">
      Search and paste previous copy entries</font></li>
      <li>
      <b><font size="3">Keep multiple computer's clipboards in sync</font></b></li>
      <li><font size="3">Data is encrypted when sent over the network</font></li>
      <li>
      <font size="3">
      Accessed from tray icon or global hot key</font></li>
      <li>
      <font size="3">
      Select entry by double click, enter key or drag 
      drop</font></li>
      <li>
      <font size="3">
      Paste into any window that excepts standard 
      copy/paste entries</font></li>
      <li>
      <font size="3">
      Display thumbnail of copied images in list
      </font></li>
      <li>
 Full Unicode support
(display foreign characters)</li>
      <li>
 UTF-8 support for language files
(create language files in any language)</li>
      <li>
 Uses sqlite database (<a href="http://www.sqlite.org">www.sqlite.org</a>)
      <font size="3">
      <br>
      </font></li>
    </ul>
