    <p style="margin-left: 4">Steps to creating a language file</p>
    <ol>
      <li>
        <p style="margin-left: 4"><a href="http://sourceforge.net/project/showfiles.php?group_id=84084">Download
        the latest version
        here</a>.</li>
      <li>
        <p style="margin-left: 4">Navigate to the language directory under the installation path (usually, C:\Program files\Ditto)</li>
      <li>
        <p style="margin-left: 4">Make a copy of English.xml and name it appropriately
        (Spanish.xml)</li>
      <li>
        <p style="margin-left: 4">Open the file in your favorite text editor.</li>
      <li>
        <p style="margin-left: 4">For each item place the translated text in the
        tag.&nbsp; Your translating the text in &quot;English_Text&quot;.&nbsp;&nbsp;
        Do Not change the ID or English Text&nbsp;</li>
      <li>
        <p style="margin-left: 4">
        
        
        <b><font size="4">ex. &lt;Item English_Text = "Groups" ID =
        &quot;-1&quot;&gt;<i>Replacement for &quot;Groups&quot; goes here</i>&lt;/Item></font></b></li>
      <li>
        <p style="margin-left: 4">Save the file in the language directory, then restart Ditto and on the General Tab there is a
        language combo.  Select your language and hit ok.  Now you should see your language when you view Ditto.  You can still edit your file but you will need to restart Ditto to view you changes.</li>
      <li>
        <p style="margin-left: 4">For languages that require Unicode characters
        save the file as UTF-8</li>
      <li>
        <p style="margin-left: 4">When your finished with a language file email the files to me (sabrogden@yahoo.com) so I can put them on the website and include them in a upcoming release.</li>
    </ol>

